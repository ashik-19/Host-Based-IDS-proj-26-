def get_baseline_hash(filepath):
    """
    Returns the stored baseline hash for a given filepath.
    Returns None if no baseline exists for this file yet.
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute('SELECT hash FROM baselines WHERE filepath = ?', (filepath,))
    row = cursor.fetchone()
    conn.close()
    return row['hash'] if row else None


def set_baseline_hash(filepath, hash_value):
    """
    Inserts or updates the baseline hash for a given filepath.
    Uses INSERT OR REPLACE so it works for both new and existing files.
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute('''
        INSERT OR REPLACE INTO baselines (filepath, hash, last_updated)
        VALUES (?, ?, ?)
    ''', (filepath, hash_value, datetime.now().strftime('%Y-%m-%d %H:%M:%S')))
    conn.commit()
    conn.close()


def get_all_baselines():
    """
    Returns all baseline entries — used to display the monitored file list.
    """
    conn = get_connection()
    cursor = conn.cursor()
    cursor.execute('SELECT * FROM baselines ORDER BY filepath')
    rows = cursor.fetchall()
    conn.close()
    return [dict(row) for row in rows]


